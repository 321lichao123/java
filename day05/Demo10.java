class Demo10 {
	public static void main(String[] args) {
		// System.out.println("Hello World!");
		/**
			二维数组格式：
				数据类型 数组名[][] = new 数据类型[m][n];
				数据类型[] 数组名[] = new 数据类型[m][n];

				int[][] arr = new int[3][2];
				[[I@5fd1358f
				[I@2013706e
				0
				
		*/
		int[][] arr = new int[3][2];
		System.out.println(arr);
		System.out.println(arr[0]);
		System.out.println(arr[0][0]);
	}
}
